//Concatenar Caracteres

var nombre = "Juan";
var apellido = "Perez";

var nombrecompleto = nombre + " " + apellido;
console.log(nombrecompleto);

var nombrecompleto2 = "Carlos" + " " + "Lara";
console.log(nombrecompleto2);

var x = nombre + 2 + 4;
console.log(x);

var x = nombre + (2 + 4);
console.log(x);

var x = 2 + 4 + nombre;
console.log(x);
